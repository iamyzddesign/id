document.getElementById('buyButton1').addEventListener('click', function() {
  // Ganti '628xxxxxxxxxx' dengan nomor WhatsApp penerima
  var phoneNumber = '6281218141079';
  
  // Ganti 'Halo! Saya tertarik dengan produk Anda.' dengan pesan yang diinginkan
  var message = 'Halo! Saya tertarik dengan produk Anda yang ini.';
  
  // Ganti 'https://example.com/product-image.jpg' dengan URL gambar produk
  var productImageURL = 'https://iamyzddesign.github.io/id/GRAPHIC%20DESIGNER%20T-SHIRT.jpg';
  
  // Membuka link WhatsApp dengan pesan dan gambar yang ditentukan
  window.open('https://api.whatsapp.com/send?phone=' + phoneNumber + '&text=' + encodeURIComponent(message) + '&source=&data=' + encodeURIComponent(productImageURL));
});

